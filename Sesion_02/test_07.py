"""Operadores aritméticos con Python"""

"""Operadores Resta"""
"""Y saber qué tipo de dato es que tenemos al realizar estas operaciones"""
"""Usando: type()"""

resta1 = 20 - 100
resta2 = 3000 - 500
resta3 = 200.96 - 3500 - 460

print("El tipo de dato de mi variable resta1 es: {}".format(type(resta1)))
print("El tipo de dato de mi variable resta2 es: {}".format(type(resta2)))
print("El tipo de dato de mi variable resta3 es: {}".format(type(resta3)))
