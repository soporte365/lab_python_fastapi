"""Operadores Suma"""
"""Y saber qué tipo de dato es que tenemos al realizar estas operaciones"""
"""Usando: type()"""

suma1 = 50 + 200
suma2 = 1500 + 2500
suma3 = 20.96 + 500 + 1590

print("El tipo de dato de mi variable suma1 es: {}".format(type(suma1)))
print("El tipo de dato de mi variable suma2 es: {}".format(type(suma2)))
print("El tipo de dato de mi variable suma3 es: {}".format(type(suma3)))


